#include <iostream>
using namespace std;

int main() {
    // Declare variables
    int num1, num2, sum;

    // Input two numbers
    cin >> num1 >> num2;

    // Perform addition
    sum = num1 + num2;

    // Output the result
    cout << "The sum of " << num1 << " and " << num2 << " is: " << sum << endl;

    return 0;
}